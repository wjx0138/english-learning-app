def create_word_entry(index: int, word: str, phonetic: str, definition: str, level: str, base_difficulty: int) -> Dict:
    """åˆ›å»ºå•ä¸ªè¯æ±‡æ¡ç›®"""
    difficulty = min(5, base_difficulty + (index // 100))

    # ç¡®å®šè¯æ€§
    if definition.startswith("v."):
        pos = "verb"
    elif definition.startswith("n."):
        pos = "noun"
    elif definition.startswith("adj."):
        pos = "adjective"
    elif definition.startswith("adv."):
        pos = "adverb"
    else:
        pos = "noun"

    return {
        "id": f"{level}_{index:04d}",
        "word": word,
        "phonetic": f"/{phonetic}/",
        "definition": definition,
        "examples": [
            f"Usage example for '{word}'."
        ],
        "synonyms": [],
        "antonyms": [],
        "difficulty": difficulty,
        "tags": [level, pos],
        "etymology": f"Etymology for {word}"
    }


